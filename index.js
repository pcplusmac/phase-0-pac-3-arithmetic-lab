
//define function add()
function add(a,b){
    return a+b;
}

// Define function substract
function subtract(a,b){
    return a-b;
}

// Define multiply()
function multiply(a,b){
    return a*b;
}

// Define function divide()
function divide(a,b) {
    return a/b;
}

// Define increment function
function increment(n) {
    return n+=1;
}