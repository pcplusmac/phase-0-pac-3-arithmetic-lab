
//define function add()
function add(a,b){
    return a+b;
}

// Define function substract
function subtract(a,b){
    return a-b;
}

// Define multiply()
function multiply(a,b){
    return a*b;
}

// Define function divide()
function divide(a,b) {
    return a/b;
}

// Define increment function
function increment(n) {
    return n+=1;
}

// Define decrement function
function decrement(n) {
    return n-=1;
}

// Define a function to parse the string into integer
function makeInt(n) {
    let num = parseInt(n,10);
    return num;
}